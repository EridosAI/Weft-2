"""Pose search for items affected by cross-room DINOv2 bleed (Phase 2 substrate fix).

Per the 2026-05-14 reviewer directive following the third STOP in
session 5: for items the extended diagnostic flagged as showing
out-of-scope DINOv2 sensitivity (cross-room bleed from LivingRoom
retexturing), generate candidate viewing poses around the item's
centroid, screen each candidate against:

  1. NavMesh navigability — the candidate's viewing position must be
     within tolerance of a reachable point.
  2. DINOv2 stability under RandomizeMaterials — the candidate's DINOv2
     before-vs-after cosine, averaged across multiple
     RandomizeMaterials draws, must be near 1.0 (the item's
     representation is NOT sensitive to LivingRoom retexturing from
     this pose).

Candidates are generated by sweeping the angle θ around the centroid;
the agent position is `centroid - viewing_distance_m * (sin(θ), cos(θ))`
in (X, Z), and the agent's heading is θ (so the agent faces the
centroid). This preserves the geometric "enters → fills → slides out"
pattern of the close-up segment — only the angle of approach changes.

Output: a candidate report at
`results/inner_pam_v0/phase2_pose_search/pose_search_report.{md,json}`
with the ranked candidates per affected item. The reviewer (or this
script's caller) picks the best surviving candidate, then re-runs
session-4 calibration + the preflight against the adjusted geometry.

Usage:
  nohup python3.12 -u scripts/run_phase2_pose_search.py \\
      > logs/phase2_pose_search_$(date +%Y%m%d_%H%M%S).log 2>&1 &
"""

